    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        // TODO add your handling code here:
        String code=txtSearchCode.getText().trim();
        if(code.isEmpty()){
            lbWaringSearchCourse.setText("Please enter code");
        }else{
            Course course=findCourseByCode(code);
            if(course==null){
                JOptionPane.showMessageDialog(this, "Code is not exist");
                txtSearchName.setText("");
                txtSearchCredit.setText("");
            }else{
                txtSearchName.setText(course.getName());
                txtSearchCredit.setText(course.getCredit());
                lbWaringSearchCourse.setText("");
            }
        }

    }//GEN-LAST:event_btSearchActionPerformed

