    Course findCourseByCode(String code) {
        ArrayList<Course> courseList = parent.getCourseList();
        Course courseSearch = null;
        for (Course course : courseList) {
            if (course.getCode().equalsIgnoreCase(code)) {
                courseSearch = course;
                break;
            }
        }
        return courseSearch;
    }
